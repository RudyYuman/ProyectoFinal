        public List<TipoMatriculaBO> Listar()
        {
            //Actualizamos el nombre de la tabla
            List<TipoMatriculaBO> list = new List<TipoMatriculaBO>();
            TipoMatriculaBO dto = null;
            try
            {

                using (OracleConnection cn = new OracleConnection(strOracle))
                {
                    cn.Open();
                    //cambiar el nombre del store procedure
                    using (OracleCommand command = new OracleCommand("select * from TIPO_MATRICULA", cn))
                    {
                        command.CommandType = System.Data.CommandType.Text;
                        using (OracleDataReader dr = command.ExecuteReader(System.Data.CommandBehavior.CloseConnection))
                        {
                            while (dr.Read())
                            {
                                dto = new TipoMatriculaBO();
                                dto.ID_TIPO_MATRICULA = Convert.ToInt32(dr["ID_TIPO_MATRICULA"]);
                                dto.NOMBRE_MATRICULA = dr["NOMBRE_MATRICULA"].ToString();
                                dto.TIPO_MATRICULA = dr["TIPO_MATRICULA"].ToString();
                                list.Add(dto);
                            }
                        }
                    }
                }

            }
            catch (Exception ex)
            {
                new Exception("Error en el metodo Listar" + ex.Message);
            }

            return list;
        }
    }
