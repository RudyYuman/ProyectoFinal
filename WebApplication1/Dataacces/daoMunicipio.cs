        }

        public List<MunicipioBO> Listar()
        {
            //Actualizamos el nombre de la tabla
            List<MunicipioBO> list = new List<MunicipioBO>();
            MunicipioBO dto = null;
            try
            {

                using (OracleConnection cn = new OracleConnection(strOracle))
                {
                    cn.Open();
                    //cambiar el nombre del store procedure
                    using (OracleCommand command = new OracleCommand("select * from MUNICIPIO", cn))
                    {
                        command.CommandType = System.Data.CommandType.Text;
                        using (OracleDataReader dr = command.ExecuteReader(System.Data.CommandBehavior.CloseConnection))
                        {
                            while (dr.Read())
                            {
                                dto = new MunicipioBO();
                                dto.ID_MUNICIPIO = Convert.ToInt32(dr["ID_MUNICIPIO"]);
                                dto.NOMBRE_MUNICIPIO = dr["NOMBRE_MUNICIPIO"].ToString();
                                list.Add(dto);
                            }
                        }
                    }
                }

            }
            catch (Exception ex)
            {
                new Exception("Error en el metodo Listar" + ex.Message);
            }

            return list;
        }
    }
}
